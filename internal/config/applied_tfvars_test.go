// TestWriteAppliedTFVars_AtomicWriteAndMode is the integration-y test:
// writes a real snapshot, verifies path, mode, content. Hits the
// per-phase state dir routing.
func TestWriteAppliedTFVars_AtomicWriteAndMode(t *testing.T) {
	ws, _ := stageWorkspace(t)
	dir := t.TempDir()
	src := filepath.Join(dir, "terraform.tfvars")
	if err := os.WriteFile(src, []byte("region = \"us-east-1\"\n"), 0o600); err != nil {
		t.Fatal(err)
	}

	if err := WriteAppliedTFVars(ws, "trial", []string{src}); err != nil {
		t.Fatal(err)
	}
	p, err := AppliedTFVarsPath(ws, "trial")
	if err != nil {
		t.Fatal(err)
	}
	st, err := os.Stat(p)
	if err != nil {
		t.Fatalf("snapshot not written: %v", err)
	}
	// Mode must be 0600 — secrets-grade.
	if mode := st.Mode().Perm(); mode != 0o600 {
		t.Errorf("snapshot mode = %o, want 0600", mode)
	}
	body, err := os.ReadFile(p)
	if err != nil {
		t.Fatal(err)
	}
	if !strings.Contains(string(body), `region = "us-east-1"`) {
		t.Errorf("snapshot missing expected value:\n%s", body)
	}
	// Atomic-rename leaves no temp files behind in the same dir.
	entries, err := os.ReadDir(filepath.Dir(p))
	if err != nil {
		t.Fatal(err)
	}
	for _, e := range entries {
		if strings.HasPrefix(e.Name(), ".terraform.applied.tfvars.") {
			t.Errorf("temp file left behind: %s", e.Name())
		}
	}
}

// TestAppliedTFVarsPath_PhaseRouting confirms cluster/trial/legacy-single
// land in the right per-phase state dirs.
func TestAppliedTFVarsPath_PhaseRouting(t *testing.T) {
	ws, _ := stageWorkspace(t)

	cluster, err := AppliedTFVarsPath(ws, "cluster")
	if err != nil {
		t.Fatal(err)
	}
	if !strings.Contains(cluster, "state-cluster") {
		t.Errorf("cluster phase: expected path under state-cluster/, got %s", cluster)
	}

	trial, err := AppliedTFVarsPath(ws, "trial")
	if err != nil {
		t.Fatal(err)
	}
	if strings.Contains(trial, "state-cluster") || !strings.Contains(trial, "/state/") {
		t.Errorf("trial phase: expected path under state/, got %s", trial)
	}

	legacy, err := AppliedTFVarsPath(ws, "legacy-single")
	if err != nil {
		t.Fatal(err)
	}
	if legacy != trial {
		t.Errorf("legacy-single should reuse the trial state dir; got %s vs trial %s", legacy, trial)
	}

	// Unknown phase falls through to trial — defensive fallback.
	unknown, err := AppliedTFVarsPath(ws, "wat-is-this")
	if err != nil {
		t.Fatal(err)
	}
	if unknown != trial {
		t.Errorf("unknown phase should fall through to trial dir; got %s vs trial %s", unknown, trial)
	}
}

// TestSourceLabel maps the basename → friendly-name table.
func TestSourceLabel(t *testing.T) {
	t.Parallel()
	cases := map[string]string{
		"/abs/path/terraform.tfvars":          "config.yaml",
		"terraform.tfvars":                    "config.yaml",
		"/home/u/terraform.tfvars.user":       "terraform.tfvars.user",
		"/some/other/file.tfvars":             "file.tfvars",
		"/abs/path/with spaces/custom.tfvars": "custom.tfvars",
	}
	for input, want := range cases {
		if got := sourceLabel(input); got != want {
			t.Errorf("sourceLabel(%q) = %q, want %q", input, got, want)
		}
	}
}

// TestAppliedTFVarsVersion_Fallback ensures the "dev" fallback kicks
// in when SetAppliedTFVarsVersion was never called (test binary that
// doesn't import the CLI package).
func TestAppliedTFVarsVersion_Fallback(t *testing.T) {
	// Save + restore the seam so we don't leak into other tests.
	prev := appliedTFVarsVersionFn
	appliedTFVarsVersionFn = nil
	defer func() { appliedTFVarsVersionFn = prev }()

	if got := appliedTFVarsVersion(); got != "dev" {
		t.Errorf("fallback version = %q, want \"dev\"", got)
	}

	SetAppliedTFVarsVersion(func() string { return "v9.9.9" })
	if got := appliedTFVarsVersion(); got != "v9.9.9" {
		t.Errorf("wired version = %q, want \"v9.9.9\"", got)
	}

	// Empty return falls through to "dev".
	SetAppliedTFVarsVersion(func() string { return "" })
	if got := appliedTFVarsVersion(); got != "dev" {
		t.Errorf("empty-return version = %q, want \"dev\"", got)
	}
}
