// helper: stage a workspace dir + state-dir layout under a private
// ROKSBNKCTL_HOME for one test, return (workspace name, cleanup).
func stageWorkspace(t *testing.T) (string, string) {
	t.Helper()
	home := t.TempDir()
	t.Setenv(ROKSBNKCTLHomeEnv, home)
	ws := "appliedtest"
	wsDir, err := WorkspaceDir(ws)
	if err != nil {
		t.Fatal(err)
	}
	if err := os.MkdirAll(wsDir, 0o750); err != nil {
		t.Fatal(err)
	}
	// Pre-create the per-phase state dirs so WriteAppliedTFVars's
	// MkdirAll is a no-op and we can stat the output.
	stateDir, err := WorkspaceStateDir(ws)
	if err != nil {
		t.Fatal(err)
	}
	if err := os.MkdirAll(stateDir, 0o750); err != nil {
		t.Fatal(err)
	}
	clusterStateDir, err := WorkspaceClusterStateDir(ws)
	if err != nil {
		t.Fatal(err)
	}
	if err := os.MkdirAll(clusterStateDir, 0o750); err != nil {
		t.Fatal(err)
	}
	return ws, home
}

// TestRenderAppliedTFVars_HeaderShape pins the exact header bytes so a
// reader (or another tool) can rely on the format.
func TestRenderAppliedTFVars_HeaderShape(t *testing.T) {
	t.Parallel()
	now := time.Date(2026, 5, 18, 12, 0, 0, 0, time.UTC)
	got, err := renderAppliedTFVars("trial", nil, now, "v0.9.0-rc1")
	if err != nil {
		t.Fatal(err)
	}
	want := "# Generated by awsbnkctl v0.9.0-rc1 at 2026-05-18T12:00:00Z after terraform apply on phase=trial.\n" +
		"# Re-generated each apply. Do not edit by hand — your changes will be overwritten.\n\n"
	if got != want {
		t.Errorf("header mismatch:\ngot:\n%s\nwant:\n%s", got, want)
	}
}

// TestRenderAppliedTFVars_AssignmentsSortedAndAttributed covers the
// happy path: assignments from one source are alphabetically sorted
// under a "from <source>" section header.
func TestRenderAppliedTFVars_AssignmentsSortedAndAttributed(t *testing.T) {
	t.Parallel()
	dir := t.TempDir()
	src := filepath.Join(dir, "terraform.tfvars")
	body := `# comment line — ignored
region = "us-east-1"
vpc_id = "vpc-abc123"
cluster_name = "bnk-prod"
`
	if err := os.WriteFile(src, []byte(body), 0o600); err != nil {
		t.Fatal(err)
	}

	now := time.Date(2026, 5, 18, 0, 0, 0, 0, time.UTC)
	got, err := renderAppliedTFVars("trial", []string{src}, now, "test")
	if err != nil {
		t.Fatal(err)
	}
	// Section header uses the basename mapped through sourceLabel.
	if !strings.Contains(got, "# === from config.yaml ===") {
		t.Errorf("missing section header in output: %s", got)
	}
	// Assignments alphabetically sorted (cluster_name < region < vpc_id).
	idxCluster := strings.Index(got, "cluster_name")
	idxRegion := strings.Index(got, "region")
	idxVPC := strings.Index(got, "vpc_id")
	if !(idxCluster > 0 && idxCluster < idxRegion && idxRegion < idxVPC) {
		t.Errorf("assignments not alphabetic: cluster_name=%d region=%d vpc_id=%d\n%s",
			idxCluster, idxRegion, idxVPC, got)
	}
}

// TestRenderAppliedTFVars_RedactsAWSStaticCreds confirms the AWS
// equivalent of upstream's ibmcloud_api_key redaction.
func TestRenderAppliedTFVars_RedactsAWSStaticCreds(t *testing.T) {
	t.Parallel()
	dir := t.TempDir()
	src := filepath.Join(dir, "terraform.tfvars")
	body := `region = "us-east-1"
aws_access_key_id = "AKIAIOSFODNN7EXAMPLE"
aws_secret_access_key = "wJalrXUtnFEMI/K7MDENG/bPxRfiCYEXAMPLEKEY"
aws_session_token = "FwoGZXIvYXdz..."
vpc_id = "vpc-abc"
`
	if err := os.WriteFile(src, []byte(body), 0o600); err != nil {
		t.Fatal(err)
	}

	now := time.Date(2026, 5, 18, 0, 0, 0, 0, time.UTC)
	got, err := renderAppliedTFVars("trial", []string{src}, now, "test")
	if err != nil {
		t.Fatal(err)
	}
	// None of the secret values must appear.
	for _, secret := range []string{"AKIAIOSFODNN7EXAMPLE", "wJalrXUtnFEMI", "FwoGZXIvYXdz"} {
		if strings.Contains(got, secret) {
			t.Errorf("secret material leaked into snapshot: %s\noutput:\n%s", secret, got)
		}
	}
	// All three redacted vars must appear with the redaction placeholder.
	for _, name := range []string{"aws_access_key_id", "aws_secret_access_key", "aws_session_token"} {
		want := name + ` = "<redacted>"`
		if !strings.Contains(got, want) {
			t.Errorf("expected redacted line %q in output:\n%s", want, got)
		}
	}
	// Non-redacted vars must still appear with their values.
	if !strings.Contains(got, `region = "us-east-1"`) {
		t.Errorf("expected non-redacted region in output:\n%s", got)
	}
}

